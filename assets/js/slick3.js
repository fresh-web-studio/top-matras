$(function () {
    $('.one-item-1').click(function () {
        $('.one-item-1').addClass('one-active');
        $('.two-item-1').removeClass('two-active');
        $('.three-item-1').removeClass('three-active');
    });
    $('.two-item-1').click(function () {
        $('.two-item-1').addClass('two-active');
        $('.one-item-1').removeClass('one-active');
        $('.three-item-1').removeClass('three-active');
    });
    $('.three-item-1').click(function () {
        $('.three-item-1').addClass('three-active');
        $('.one-item-1').removeClass('one-active');
        $('.two-item-1').removeClass('two-active');
    });
});

